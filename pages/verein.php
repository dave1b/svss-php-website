 <h3 class="articleTitle">Wie alles begann</h3>
  <p>Der Sportverein Sempachersee pflegt seit über 75 Jahren den Sport in seiner ganzen Vielfalt in den 
    entsprechenden Alters- & Fähigkeitsstufen.<br> Der Verein wurde ursprünglich in Nottwil als Sportverein Nottwil im Jahr 1940 gegründet und zählte damals einige
    Mitglieder aus der Gemeinde. Aufgrund des starken Engagements der Mitglieder wurde es jedoch schnell zu einem grösserem Verein in Nottwil. 
    Später fusionierte der Verein dann mit 3 weiteren aus der Umgebung zum Sportverein Sempachersee (SVSS). <br><br>Durch diesen Anstieg wuchsen
    auch die finanziellen Ressourcen und so baute der Verein die Tennishalle in Schenkon. Bis heute hat sich der Verein Sempachersee weiterentwickelt
    und ist heute weiterhing als der Sportverein der Region bekannt.
    <br><br>
    Heute hat der SVSS den Hauptsitz in Sursee und zählt momentan rund 450 Mitglieder. Durch unser 
    aktives Engagement konnten wir in den letzten 4 Jahren 115 neue Mitglieder zählen und danken dafür allen die mitgeholfen haben. 

    </p>  
